export function getProductsByCategory(categoryName: string): Product[] {
  return products.filter(p => p.category === categoryName);
}

export function getProductsBySubcategory(categoryName: string, subcat: string): Product[] {
  return products.filter(p => p.category === categoryName && p.subcategory === subcat);
}

export function getProductsNoSubcat(categoryName: string): Product[] {
  return products.filter(p => p.category === categoryName && !p.subcategory);
}

export function formatPrice(price: number | null, currency: string): string {
  if (price === null) return "Consultar precio";
  if (currency === "USD") return `USD ${price.toLocaleString("es-AR")}`;
  return `$ ${price.toLocaleString("es-AR")}`;
}

